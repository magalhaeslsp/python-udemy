print(1234)

# Aspas simples
print('Lucas Magalhães')
print(1, 'Lucas "Magalhães"')

# Aspas duplas
print("Lucas Magalhães")
print(2, "Lucas 'Magalhães'")

# Escape
print("Lucas \"Magalhães\"") # Colocando a barra invertida (\) ele considera o proximo caracter como parte do texto

# r
print(r"Lucas \"Magalhães\"")
